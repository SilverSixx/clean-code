package DesignPattern.CreationalPattern.AbstractFactory;

// The Abstract Factory Pattern is a creational design pattern
// that provides an interface for creating families of related or dependent objects
// without specifying their concrete classes

// This pattern is particularly useful when you need to create objects
// that are related to each other, but you want to decouple their creation from the client code

// Senario
// Assume we have different types of notification systems (e.g., email, SMS, push notifications)
// and we want to support multiple families of products,
// such as basic notifications and premium notifications with additional features.